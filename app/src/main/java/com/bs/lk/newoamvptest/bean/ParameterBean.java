public class ParameterBean {
    private String nums;

    public String getNums() {
        return nums;
    }

    public void setNums(String nums) {
        this.nums = nums;
    }

    @Override
    public String toString() {
        return "ParameterBean{" +
                "nums='" + nums + '\'' +
                '}';
    }
}
